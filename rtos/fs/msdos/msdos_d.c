#include <fs/util.h>

#include "fs.h"
#include "dentry.h"
#include "dentryop.h"
#include "msdos/msdos_d.h"

static int msdos_dentry_compare(struct dentry *den,
				struct qstr *a, struct qstr *b)
{
  //cprintf("A value: '%s'\n",QSTRNAME(a));
  //cprintf("B value: '%s'\n",QSTRNAME(b));
  return strcasecmp(QSTRNAME(a),QSTRNAME(b));
}

struct dentry_operations msdos_dentry_operations={
  msdos_dentry_compare
};
